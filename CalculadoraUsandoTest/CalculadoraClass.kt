
class CalculadoraClass {

        companion object{

            fun som(n1: Int, n2: Int): Int{
                return n1 + n2
            }

            fun sub(n1: Int, n2: Int): Int{
                return n1 - n2
            }

            fun mult(n1: Int, n2: Int): Int{
                return n1 * n2
            }

            fun div (n1: Int, n2: Int): Int{
                return n1 / n2

            }

        }

    }
}